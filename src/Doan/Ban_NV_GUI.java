    public void addbutton()
    {
        BanBLL ban = new BanBLL();
        ArrayList<Ban_DTO> arr = new ArrayList<Ban_DTO>();
        arr = ban.getAllBan();
        int lenth = (int) arr.size()/3;
        int dem = 0, x = 70, y =70;
        for(int i = 0; i < 3; i++)
        {
            for(int j = 0; j<lenth; j++)
            {
                JButton bt = new JButton();
                Ban_DTO ban_DTO = arr.get(dem);
                dem++;
                if(ban_DTO.getTrangthai().equals("Đã đặt"))
                {
                    bt.setBackground(Color.red);
                    Icon icon = new ImageIcon("src\\Doan\\images\\chuquan\\icons8_table_26px.png");
                    bt.setIcon(icon);
                    bt.setSize(90, 90);
                    y = i*90;
                    x = j*90;
                    bt.setLocation(x, y);
                    bt.setText(ban_DTO.getMaban());
                    this.Panel_DS_Ban.add(bt);
                
                }
                else
                {
                    bt.setBackground(Color.white);
                    Icon icon = new ImageIcon("src\\Doan\\images\\chuquan\\icons8_table_26px.png");
                    bt.setIcon(icon);
                    bt.setSize(90, 90);
                    y = i*90;
                    x = j*90;
                    bt.setLocation(x, y);
                    bt.setText(ban_DTO.getMaban());
                    this.Panel_DS_Ban.add(bt);
                }
            }
        
        }
        
    }

